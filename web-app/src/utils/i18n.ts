const DICTS: Record<AppLanguage, Dict> = {
  en: EN,
  fr: FR,
  es: ES,
  pt: PT,
  sw: SW,
  ha: HA,
  yo: YO,
  'pcm-NG': PCM,
};

export function t(lang: string, key: string, vars?: Record<string, string | number>): string {
  const typed = (DICTS[lang as AppLanguage] ?? EN);
  const template = typed[key] ?? EN[key] ?? key;
  if (!vars) return template;
  return Object.entries(vars).reduce(
    (acc, [k, v]) => acc.replaceAll(`{{${k}}}`, String(v)),
    template,
  );
}

export function getSpeechLocale(lang: string): string {
  return LANGUAGE_OPTIONS.find((x) => x.code === lang)?.speechLocale ?? 'en-US';
}
